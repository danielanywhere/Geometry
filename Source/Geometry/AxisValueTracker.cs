	}
	//*-------------------------------------------------------------------------*

	//*-------------------------------------------------------------------------*
	//*	AxisValueTrackerItem																										*
	//*-------------------------------------------------------------------------*
	/// <summary>
	/// Individual axis value tracker assignment.
	/// </summary>
	public class AxisValueTrackerItem
	{
		//*************************************************************************
		//*	Private																																*
		//*************************************************************************
		//*************************************************************************
		//*	Protected																															*
		//*************************************************************************
		//*************************************************************************
		//*	Public																																*
		//*************************************************************************
		//*-----------------------------------------------------------------------*
		//*	AxisName																															*
		//*-----------------------------------------------------------------------*
		/// <summary>
		/// Private member for <see cref="AxisName">AxisName</see>.
		/// </summary>
		private string mAxisName = "";
		/// <summary>
		/// Get/Set the name of the axis.
		/// </summary>
		public string AxisName
		{
			get { return mAxisName; }
			set { mAxisName = value; }
		}
		//*-----------------------------------------------------------------------*

		//*-----------------------------------------------------------------------*
		//*	NewValue																															*
		//*-----------------------------------------------------------------------*
		/// <summary>
		/// Private member for <see cref="NewValue">NewValue</see>.
		/// </summary>
		private float mNewValue = 0f;
		/// <summary>
		/// Get/Set the new value for the axis.
		/// </summary>
		public float NewValue
		{
			get { return mNewValue; }
			set { mNewValue = value; }
		}
		//*-----------------------------------------------------------------------*

		//*-----------------------------------------------------------------------*
		//*	OriginalValue																													*
		//*-----------------------------------------------------------------------*
		/// <summary>
		/// Private member for <see cref="OriginalValue">OriginalValue</see>.
		/// </summary>
		private float mOriginalValue = 0f;
		/// <summary>
		/// Get/Set the original value for the axis.
		/// </summary>
		public float OriginalValue
		{
			get { return mOriginalValue; }
			set { mOriginalValue = value; }
		}
		//*-----------------------------------------------------------------------*


	}
	//*-------------------------------------------------------------------------*

}
